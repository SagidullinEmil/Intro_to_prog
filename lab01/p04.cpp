#include <iostream>

int  main(){
    using namespace  std;
    int x, y;
    cout << "Enter the first intager: "<<endl;
    cin >> x;
    cout << "Enter the second intager: "<<endl;
    cin >> y;

    cout << x << "+" << y << '=' << x+y<<endl;
    cout << x << "-" << y << '=' << x-y<<endl;
    cout << x << "*" << y << '=' << x*y<<endl;
    cout << x << "/" << y << '=' << x/y<<endl;
    cout << x << "%" << y << '=' << x%y<<endl;

}


